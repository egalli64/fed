let value = 2;

console.log('value is', typeof value, value);
console.log('value is strictly number 2?', value === 2);
console.log('value is strictly different from number 3?', value !== 3);
console.log('value is less than number 2?', value < 2);
console.log('value is less or equal to number 2?', value <= 2);
console.log('value is greater than number 2?', value > 2);
console.log('value is greater or equal to number 2?', value >= 2);
console.log('value is equal to string "2"?', value == '2');
console.log('value is not equal to string "2"?', value != '2');